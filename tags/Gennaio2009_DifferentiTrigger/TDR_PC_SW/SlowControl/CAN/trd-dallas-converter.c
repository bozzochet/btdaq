void reincode(void) {

  int i, j, k;
  
  printf("// file trddef.h \n"        );
  printf("// \n"                      );
  printf("// TRD Dallas sensor IDs \n");
  printf("// \n"                      );
  printf("// A.Lebedev \n"            );
  printf("// \n"                      );
  printf("// Created by trd-dallas-converter.c \n");
  printf("// \n"                      );
  printf(" \n"                        );
  printf("//~---------------------------------------------------------------------------- \n");
  printf(" \n"                 );
  printf("#ifndef _TRDDEF_H \n");
  printf("#define _TRDDEF_H \n");
  printf(" \n"                 );
  printf("//~ - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - \n");
  printf(" \n"                    );
  printf("typedef struct {\n"     );
  printf("  char* name;\n"        );
  printf("  int   n;\n"           );
  printf("  int64 ID[32];\n"      );
  printf("} _chain;\n"            );
  printf(" \n"                    );
  printf("_chain chain[40] = {\n" );

  for (k=0; k<40; k++) {
    struct _strip *p = &strip[k];
    printf("{\"%s\", %2d,\n", p->name, p->n);
    for (j=0; j<p->n; j++) {
      int64 ID;
      printf("  %c0x", j ? ' ' : '{');
//printf("\n");
      ID = 0x0000000000000000LL;
//for (i=0; i<8; i++) {
//printf("%02hhX ", p->ID[j][i]);
//}
//printf("\n");
      for (i=0; i<8; i++) {
        ID |= (int64)p->ID[j][i] << (8 * i);
//printf("%016llX\n", ID);
      }
//printf("%016llX\n", ID);
//exit(1);
      printf("%016llXLL", ID);
      if (j < (p->n - 1)) {
        printf(",\n");
      }
      else {
        printf("}\n");
        if (k < (40 - 1)) printf("},\n");
        else              printf("}};\n");
      }
    }
  }
  
  printf("#endif // _PDSDEF_H \n");
}

