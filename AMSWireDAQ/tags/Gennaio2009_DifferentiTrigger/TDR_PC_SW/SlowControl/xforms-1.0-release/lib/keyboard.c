#include "forms.h"

void
fl_ringbell(int percent)
{
    if (percent < -100)
	percent = -100;
    else if (percent > 100)
	percent = 100;

    if (flx->display)
	XBell(flx->display, percent);
    else
	fprintf(stderr, "\a");
}
